const toString = require('./to-string');

const rows = [
  [2, 6, 0, 0, 1, 0, 0, 3, 0],
  [3, 0, 0, 0, 0, 0, 0, 0, 8],
  [7, 1, 8, 0, 0, 0, 5, 2, 4],
  [0, 0, 5, 6, 0, 7, 4, 0, 0],
  [0, 0, 0, 0, 4, 0, 0, 0, 0],
  [0, 0, 6, 5, 0, 3, 1, 0, 0],
  [8, 3, 1, 0, 0, 0, 2, 4, 7],
  [5, 0, 0, 0, 0, 0, 0, 0, 1],
  [0, 9, 0, 0, 7, 0, 0, 8, 5],
];

describe('toString', () => {
  it('should return a stringified version', () => {
    expect(toString(rows)).toBe(
      `2 6   |   1   |   3  \n3     |       |     8\n7 1 8 |       | 5 2 4\n---------------------\n    5 | 6   7 | 4    \n      |   4   |      \n    6 | 5   3 | 1    \n---------------------\n8 3 1 |       | 2 4 7\n5     |       |     1\n  9   |   7   |   8 5\n`
    );
  });
});
